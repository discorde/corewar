#include		"libft.h"

char			*ft_itoa(int n)
{
	size_t		i;
	char		buff[12];

	i = 10;
	ft_bzero(buff, 12);
	while ((n / 10) > 0)
	{
		buff[i] = ABS((n % 10) + '0');
		--i;
		n /= 10;
	}
	buff[i] = (n % 10) + '0';
	if (n < 0)
	{
		--i;
		buff[i] = '-';
	}
	return (ft_strdup(buff + i));
}
